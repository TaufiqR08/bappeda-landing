  document.addEventListener("DOMContentLoaded", function () {
    const days = ["Minggu", "Senin", "Selasa", "Rabu", "Kamis", "Jumat", "Sabtu"];
    const months = ["Januari", "Februari", "Maret", "April", "Mei", "Juni", 
                    "Juli", "Agustus", "September", "Oktober", "November", "Desember"];
    
    function updateDateTime() {
      const now = new Date();
      const currentDay = days[now.getDay()];
      const currentDate = now.getDate();
      const currentMonth = months[now.getMonth()];
      const currentYear = now.getFullYear();
      const currentTime = now.toLocaleTimeString("id-ID", { hour: '2-digit', minute: '2-digit', second: '2-digit' });

      const displayText = `${currentDay}, ${currentDate} ${currentMonth} ${currentYear} - ${currentTime}`;
      
      const dateTimeElement = document.getElementById("current-date-time");
      dateTimeElement.innerHTML = `<i class="bi bi-clock me-1"></i> ${displayText}`;
    }

    // Jalankan fungsi updateDateTime setiap 1 detik
    setInterval(updateDateTime, 1000);

    // Panggil fungsi sekali untuk menampilkan waktu langsung saat halaman dimuat
    updateDateTime();
  });
</script>


